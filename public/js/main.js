function basicSearch()
	{
		document.getElementById('special_search_form').style.display = 'none';
		document.getElementById('basic_search_form').style.display = 'block';
	}

function specialSearch()
	{
		document.getElementById('basic_search_form').style.display = 'none';
		document.getElementById('special_search_form').style.display = 'block';
	}
